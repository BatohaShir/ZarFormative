function getKhorooCoordinates(districtLat: number, districtLng: number, khorooNumber: number, totalKhoroos: number): { lat: number; lng: number } {
  // –†–∞—Å–ø—Ä–µ–¥–µ–ª—è–µ–º —Ö–æ—Ä–æ–æ –≤ —Å–µ—Ç–∫–µ –≤–æ–∫—Ä—É–≥ —Ü–µ–Ω—Ç—Ä–∞ –¥“Ø“Ø—Ä—ç–≥–∞
  const gridSize = Math.ceil(Math.sqrt(totalKhoroos));
  const row = Math.floor((khorooNumber - 1) / gridSize);
  const col = (khorooNumber - 1) % gridSize;

  // –°–º–µ—â–µ–Ω–∏–µ ~0.01 –≥—Ä–∞–¥—É—Å–∞ (~1 –∫–º)
  const offset = 0.008;
  const centerOffset = (gridSize - 1) / 2;

  return {
    lat: districtLat + (row - centerOffset) * offset,
    lng: districtLng + (col - centerOffset) * offset
  };
}

